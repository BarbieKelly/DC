// > Greater
// < less
// == equal
// != not equal
// >= greater or equal
// <= less or equal

var x = "Marvel";
var y = "DC";


if (x==y) {
	console.log("Marvel is equal to DC");
} else if (x>y) {
	console.log("Marvel is greater than DC");
} else if (x<y) {
	console.log("Marvel is less than DC");
} else {
	console.log("Marvel is not equal to DC");
}


